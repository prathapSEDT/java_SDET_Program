package com.rehansqapoint.loops;

public class WhileLoop {
    public static void main(String[] args) {
        //print numbers from 1-50 using a while loop

        //step1: declare a variabe with some value
        int i=1;

        while(i<=50){
            System.out.println(i);
            i=i+1;
        }


    }
}
